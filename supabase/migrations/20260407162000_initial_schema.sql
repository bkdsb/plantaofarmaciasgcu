create or replace function public.current_user_role()
returns public.user_role
language sql
stable
as $$
  select coalesce((select role from public.profiles where id = auth.uid()), 'public'::public.user_role);
$$;

create or replace function public.is_superadmin()
returns boolean
language sql
stable
as $$
  select public.current_user_role() = 'superadmin'::public.user_role;
$$;

create or replace function public.belongs_to_pharmacy(target_pharmacy_id bigint)
returns boolean
language sql
stable
as $$
  select exists (
    select 1
    from public.pharmacy_users pu
    where pu.user_id = auth.uid()
      and pu.pharmacy_id = target_pharmacy_id
  );
$$;

