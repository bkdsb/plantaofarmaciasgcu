create or replace view public.v_current_duty as
select
  ds.id as duty_id,
  ds.week_start,
  ds.week_end,
  ds.starts_at,
  ds.ends_at,
  p.id as pharmacy_id,
  p.name as pharmacy_name,
  p.slug as pharmacy_slug,
  p.address,
  p.phone,
  p.whatsapp,
  p.map_url
from public.duty_schedules ds
join public.pharmacies p on p.id = ds.pharmacy_id
where ds.status = 'published'
  and current_date between ds.week_start and ds.week_end;

create or replace view public.v_active_banners as
select *
from public.banners
where status = 'active'
  and now() between starts_at and ends_at;

