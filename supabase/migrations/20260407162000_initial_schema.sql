create or replace function public.enforce_product_limit_per_week()
returns trigger
language plpgsql
as $$
declare
  product_count integer;
begin
  if (new.status = 'active') then
    select count(*)
      into product_count
      from public.promotion_products pp
     where pp.promotion_week_id = new.promotion_week_id
       and pp.status = 'active'
       and (tg_op = 'INSERT' or pp.id <> new.id);

    if product_count >= 10 then
      raise exception 'Limite de 10 produtos ativos por semana promocional excedido';
    end if;
  end if;

  return new;
end;
$$;

create trigger trg_enforce_product_limit_per_week
before insert or update on public.promotion_products
for each row
execute function public.enforce_product_limit_per_week();

